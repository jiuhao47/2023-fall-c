#include <stdio.h>
int a[4][4] = {{0, 1, 2, 3}, {10, 11, 12, 13}, {20, 21, 22, 23}, {30, 31, 32, 33}};
int main()
{
    int(*p)[4];
    printf("a[0][0]=%d a[0][1]=%d a[0][2]=%d a[0][3]=%d\n", a[0][0], a[0][1], a[0][2], a[0][3]);
    p = &a[0];
    printf("(*p)[0]=%d (*p)[1]=%d (*p)[2]=%d (*p)[3]=%d\n", (*p)[0], (*p)[1], (*p)[2], (*p)[3]);
    printf("p[0][0]=%d p[0][1]=%d p[0][2]=%d p[0][3]=%d\n", p[0][0], p[0][1], p[0][2], p[0][3]);
    printf("(*(p+1))[0]=%d (*(p+1))[1]=%d (*(p+1))[2]=%d (*(p+1))[3]=%d\n", (*(p + 1))[0], (*(p + 1))[1], (*(p + 1))[2], (*(p + 1))[3]);
    printf("p[1][0]=%d p[1][1]=%d p[1][2]=%d p[1][3]=%d\n", p[1][0], p[1][1], p[1][2], p[1][3]);
    p = a + 2;
    printf("p[0][0]=%d p[0][1]=%d p[0][2]=%d p[0][3]=%d\n", p[0][0], p[0][1], p[0][2], p[0][3]);
    printf("p[1][0]=%d p[1][1]=%d p[1][2]=%d p[1][3]=%d\n", p[1][0], p[1][1], p[1][2], p[1][3]);
    return 0;
}